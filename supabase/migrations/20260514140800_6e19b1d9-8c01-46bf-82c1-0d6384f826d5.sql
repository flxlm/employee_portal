CREATE TABLE public.wines (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL DEFAULT '',
  domaine text NOT NULL DEFAULT '',
  year text NOT NULL DEFAULT '',
  type text NOT NULL DEFAULT '',
  country text NOT NULL DEFAULT '',
  inventory integer NOT NULL DEFAULT 0,
  colour text NOT NULL DEFAULT '',
  cost numeric NOT NULL DEFAULT 0,
  markup numeric NOT NULL DEFAULT 0,
  glass numeric NOT NULL DEFAULT 0,
  bottle numeric NOT NULL DEFAULT 0,
  togo numeric NOT NULL DEFAULT 0,
  added_by text NOT NULL DEFAULT '',
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.wines ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Wines: authenticated read" ON public.wines FOR SELECT TO authenticated USING (true);
CREATE POLICY "Wines: authenticated insert" ON public.wines FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Wines: authenticated update" ON public.wines FOR UPDATE TO authenticated USING (true) WITH CHECK (true);
CREATE POLICY "Wines: admin delete" ON public.wines FOR DELETE TO authenticated USING (has_role(auth.uid(), 'admin'::app_role));

CREATE TRIGGER wines_set_updated_at BEFORE UPDATE ON public.wines FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

